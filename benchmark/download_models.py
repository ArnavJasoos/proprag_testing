"""One-shot model downloader for the local (KoboldCpp-free) benchmark run.

Reads a Hugging Face token from ``.env`` (or the environment), then fetches the
two models the memory-safe ``benchmark.run`` pipeline needs:

  * the chat model as a pre-quantized GGUF (default gpt-oss-20b Q4_K_M), loaded
    in-process by ``llama-cpp-python`` -- no inference server, no KoboldCpp.
  * the embedder (default nvidia/NV-Embed-v2), pre-fetched into the Hugging Face
    cache so ``EmbeddingModel`` loads it offline in Phase B.

It writes ``<project>/models/model_env.sh`` with the exact env vars ``benchmark.run``
reads. Source that file, then run the benchmark:

    python -m benchmark.download_models
    source models/model_env.sh
    python -m benchmark.run --pilot 12

Env / .env keys honored:
    HF_TOKEN | HUGGING_FACE_HUB_TOKEN | HUGGINGFACE_TOKEN   gated-repo access
    PROPRAG_GGUF_REPO / PROPRAG_GGUF_FILE                    override the GGUF
    PROPRAG_EMBEDDING_MODEL                                  override the embedder
"""

from __future__ import annotations

import argparse
import os
import sys
from typing import Optional

# --- project layout -------------------------------------------------------
_HERE = os.path.dirname(os.path.abspath(__file__))
_PROJECT_DIR = os.environ.get("PROPRAG_PROJECT_DIR", os.path.dirname(_HERE))

_TOKEN_KEYS = ("HF_TOKEN", "HUGGING_FACE_HUB_TOKEN", "HUGGINGFACE_TOKEN")

_DEFAULT_GGUF_REPO = "unsloth/gpt-oss-20b-GGUF"
_DEFAULT_GGUF_FILE = "gpt-oss-20b-Q4_K_M.gguf"
_DEFAULT_EMBEDDING_MODEL = "nvidia/NV-Embed-v2"


def _load_dotenv() -> None:
    """Minimal .env loader (no dependency): populate os.environ from local .env
    files without overriding vars already set in the environment.

    Searches the project root, this package dir, and the parent (sibling repo
    layout) so a token committed to ``../.env`` is still found.
    """
    candidates = [
        os.path.join(_PROJECT_DIR, ".env"),
        os.path.join(_HERE, ".env"),
        os.path.join(os.path.dirname(_PROJECT_DIR), ".env"),
    ]
    for path in candidates:
        if not os.path.isfile(path):
            continue
        with open(path, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line or line.startswith("#") or "=" not in line:
                    continue
                k, v = line.split("=", 1)
                k, v = k.strip(), v.strip().strip('"').strip("'")
                if k and v and k not in os.environ:
                    os.environ[k] = v


def _find_token() -> Optional[str]:
    for key in _TOKEN_KEYS:
        v = os.environ.get(key)
        if v:
            return v
    return None


def _mask(tok: str) -> str:
    return f"{tok[:4]}...{tok[-4:]}" if len(tok) > 8 else "****"


def download_gguf(models_dir: str, repo: str, filename: str, token: Optional[str]) -> str:
    from huggingface_hub import hf_hub_download

    os.makedirs(models_dir, exist_ok=True)
    print(f"[gguf] downloading {repo}/{filename} -> {models_dir}")
    path = hf_hub_download(repo_id=repo, filename=filename, local_dir=models_dir, token=token)
    size_mb = os.path.getsize(path) // (1024 * 1024)
    print(f"[gguf] done: {path} ({size_mb} MB)")
    return path


def prefetch_embedder(model_name: str, token: Optional[str]) -> None:
    from huggingface_hub import snapshot_download

    print(f"[embed] pre-fetching {model_name} into the HF cache")
    snapshot_download(repo_id=model_name, token=token)
    print(f"[embed] done: {model_name} cached")


def _write_env_file(models_dir: str, gguf_path: Optional[str], gguf_file: str,
                    embedding_model: str) -> str:
    """Emit a sourceable env file wiring benchmark.run to the local models."""
    env_path = os.path.join(models_dir, "model_env.sh")
    lines = [
        "# Auto-generated by benchmark.download_models. `source` before benchmark.run.",
        f'export PROPRAG_PROJECT_DIR="{_PROJECT_DIR}"',
        "export PROPRAG_LLM_BACKEND=llama_cpp",
        "export PROPRAG_EMBEDDING_BACKEND=sentence_transformers",
        f'export PROPRAG_EMBEDDING_MODEL="{embedding_model}"',
        "export PROPRAG_EMBED_8BIT=1",
        "export PROPRAG_EMBED_MAX_LEN=512",
        "export PROPRAG_EMBED_BATCH=4",
        "export PROPRAG_LLAMA_N_GPU_LAYERS=-1",
        "export PROPRAG_LLAMA_N_CTX=8192",
    ]
    if gguf_path:
        lines.append(f'export PROPRAG_GGUF_MODEL_PATH="{gguf_path}"')
        lines.append(f'export PROPRAG_GGUF_LABEL="{gguf_file}"')
    with open(env_path, "w", encoding="utf-8", newline="\n") as f:
        f.write("\n".join(lines) + "\n")
    return env_path


def _parse_args(argv=None) -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Download the local benchmark models (chat GGUF + embedder)")
    p.add_argument("--models-dir", default=os.path.join(_PROJECT_DIR, "models"))
    p.add_argument("--gguf-repo", default=os.environ.get("PROPRAG_GGUF_REPO", _DEFAULT_GGUF_REPO))
    p.add_argument("--gguf-file", default=os.environ.get("PROPRAG_GGUF_FILE", _DEFAULT_GGUF_FILE))
    p.add_argument("--embedding-model",
                   default=os.environ.get("PROPRAG_EMBEDDING_MODEL", _DEFAULT_EMBEDDING_MODEL))
    p.add_argument("--skip-gguf", action="store_true", help="do not download the chat GGUF")
    p.add_argument("--skip-embedder", action="store_true", help="do not pre-fetch the embedder")
    return p.parse_args(argv)


def main(argv=None) -> None:
    args = _parse_args(argv)
    _load_dotenv()
    token = _find_token()
    if token:
        print(f"[auth] using HF token {_mask(token)}")
        try:
            from huggingface_hub import login

            login(token=token, add_to_git_credential=False)
        except Exception as e:  # noqa: BLE001 - login failure is non-fatal for public repos
            print(f"[auth] login warning: {e}", file=sys.stderr)
    else:
        print(f"[auth] no HF token found (looked for {', '.join(_TOKEN_KEYS)} in env/.env).\n"
              "        Public GGUF repos still work; gated models (NV-Embed-v2) will fail.",
              file=sys.stderr)

    gguf_path = None
    if not args.skip_gguf:
        gguf_path = download_gguf(args.models_dir, args.gguf_repo, args.gguf_file, token)
    if not args.skip_embedder:
        prefetch_embedder(args.embedding_model, token)

    env_path = _write_env_file(args.models_dir, gguf_path, args.gguf_file, args.embedding_model)
    print(f"\n[env] wrote {env_path}")
    print("Next:")
    print(f"  source {env_path}")
    print("  python -m benchmark.run --pilot 12")


if __name__ == "__main__":
    main()
